const CACHE_NAME = "freedom-cache-v2";
const DYNAMIC_CACHE = "freedom-dynamic-v2";

// Static assets (App Shell)
const ASSETS_TO_CACHE = [
  "/", 
  "/index.html",
  "/manifest.json",
  "/favicon.ico",
  "/er-192.png",
  "/er-512.png"
];

// 1. INSTALL: Cache Static Assets
self.addEventListener("install", event => {
  console.log("[SW] Installing...");
  self.skipWaiting();
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      return cache.addAll(ASSETS_TO_CACHE);
    })
  );
});

// 2. ACTIVATE: Clean up old caches
self.addEventListener("activate", event => {
  console.log("[SW] Activating...");
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME && key !== DYNAMIC_CACHE) {
            return caches.delete(key);
          }
        })
      )
    )
  );
  return self.clients.claim();
});

// 3. FETCH: The Smart Logic
self.addEventListener("fetch", event => {
  const url = new URL(event.request.url);

  // --- A. IGNORE NON-GET REQUESTS ---
  // We cannot cache POST (Login, Save Grade). Let them go straight to network.
  if (event.request.method !== "GET") {
    return;
  }

  // --- B. API REQUESTS (Network First, Fallback to Cache) ---
  // If requesting data (students, grades), try to get fresh data first.
  if (url.pathname.startsWith("/api/")) {
    event.respondWith(
      fetch(event.request)
        .then(response => {
          // If valid response, clone and cache it
          if (response && response.status === 200) {
            const resClone = response.clone();
            caches.open(DYNAMIC_CACHE).then(cache => {
              cache.put(event.request, resClone);
            });
          }
          return response;
        })
        .catch(() => {
          // If offline, return the cached JSON
          return caches.match(event.request).then(cachedRes => {
            if (cachedRes) return cachedRes;
            
            // If not in cache, return a fallback JSON to prevent app crash
            return new Response(JSON.stringify({ error: "You are offline" }), {
              headers: { "Content-Type": "application/json" }
            });
          });
        })
    );
    return;
  }

  // --- C. STATIC ASSETS (Cache First, Fallback to Network) ---
  // Images, JS files, CSS, Fonts
  event.respondWith(
    caches.match(event.request).then(cachedResponse => {
      if (cachedResponse) {
        return cachedResponse;
      }
      
      return fetch(event.request).then(response => {
        // Cache new static files (like main.js chunk files generated by React)
        if (!response || response.status !== 200 || response.type !== 'basic') {
          return response;
        }
        const responseToCache = response.clone();
        caches.open(CACHE_NAME).then(cache => {
          cache.put(event.request, responseToCache);
        });
        return response;
      }).catch(() => {
        // If offline and requesting a page (HTML), return index.html (App Shell)
        if (event.request.headers.get("accept").includes("text/html")) {
          return caches.match("/index.html");
        }
      });
    })
  );
});